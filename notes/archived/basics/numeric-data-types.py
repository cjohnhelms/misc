# numeric operators

a = 12
b = 3

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)   # integer division, rounded down
print(a % b)    # modulo, remainder after integer devision

# division results in a float, while integer devision will result in an int if an int is needed

